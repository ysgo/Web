package jspbean;

public class BookerInfo {
	private String bookerName;
	private String bookerColor;
	private String bookerDateTime;
	public String getBookerName() {
		return bookerName; 
	}
	public void setBookerName(String bookerName) {
		this.bookerName = bookerName;
	}
	public String getBookerColor() {
		return bookerColor;
	}
	public void setBookerColor(String bookerColor) {
		this.bookerColor = bookerColor;
	}
	public String getBookerDateTime() {
		return bookerDateTime;
	}
	public void setBookerDateTime(String bookerDateTime) {
		this.bookerDateTime = bookerDateTime;
	}	
}
